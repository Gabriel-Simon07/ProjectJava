package project_java;

public class Condicional {

	public static void main(String[] args) {
		
		short valor = 4;
		
		if(valor > 4)
			System.out.println("Valor maior que 4");
		
		else if(valor == 4)
			System.out.println("Valor igual a 4");
		
		else
			System.out.println("Valor menor que 4");
	}

}
